//4. Write a java program to find the ASCII value of the character

import java.util.*;
public class ASCIIval {
    public static void main(String[] args)
    {

        char c = 'e';
        int ascii = c;
        System.out.println("The ASCII value of " + c + " is: " + ascii);
    }
}
//Output:
//        The ASCII value of e is: 101
